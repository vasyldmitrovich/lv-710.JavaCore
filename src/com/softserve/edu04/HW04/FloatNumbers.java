package com.softserve.edu04.HW04;

public class FloatNumbers {
    public static void main(String[] args) {
        float a = 10.2F;
        float b = -5.3F;
        float c = 1005.5F;
        if (a >= - 5.5F && a <= 5.5F) {
            System.out.println( a + " належить до вказаної області");
        } else {
            System.out.println( a + " не належить до вказаної області");
        }
        if (b >= - 5.5F && b <= 5.5F) {
            System.out.println( b + " належить до вказаної області");
        } else {
            System.out.println( b + " не належить до вказаної області");
        }
        if (c >= - 5.5F && c <= 5.5F) {
            System.out.println( c + " належить до вказаної області");
        } else {
            System.out.println( c + " не належить до вказаної області");
        }

    };

    ;
}
